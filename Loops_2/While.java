public class While {
    public static void main(String[] args) {
        int i=1;
        while(i<4)
        {
            System.out.println("Hi "+ i);
            i++;
        }

        // DO WHILE LOOP

        int j=1;
        do{
            System.out.println("hello");
            j++;
        }while(j<=4);

    }    
}
