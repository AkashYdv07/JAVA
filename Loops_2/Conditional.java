// conditional statement is used when we want either this or that 
// there is a compulsion to have if you want else 
public class Conditional {
    public static void main(String[] args) {
        int x = 8;
        if(x>10)
        {
            System.out.println("Hello");
        }
        else 
        {
            System.out.println("Bye");
        }
    }
}
